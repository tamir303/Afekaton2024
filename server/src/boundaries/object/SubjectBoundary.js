class SubjectBoundary {
    /**
     * Constructs a SubjectBoundary instance.
     *
     * @param {String} subjectId - The unique identifier for the subject.
     * @param {String} name - The name of the subject.
     */
    constructor(subjectId, name) {
      this.subjectId = subjectId;
      this.name = name;
    }
  }
  
  export default SubjectBoundary;
  